  <script async>
    let wrapperEls = document.querySelectorAll('.img-wrapper');

    let headerName = document.querySelectorAll('.text-blk.name');
    let position = document.querySelectorAll('.text-blk.position');



    function handleScroll() {
      let wrapperEls = document.querySelectorAll('.img-wrapper');
      let headerNames = document.querySelectorAll('.text-blk.name');
      let position = document.querySelectorAll('.text-blk.position');


      wrapperEls.forEach(function(wrapperEl, index) {
        let rect = wrapperEl.getBoundingClientRect();

        if (rect.top >= 0 && rect.bottom <= window.innerHeight) {
          wrapperEl.style.opacity = "1";
          wrapperEl.style.transition = "opacity 1s, transform 1s";
          wrapperEl.style.transform = "translateX(0)";

          setTimeout(function() {
            if (index < headerNames.length) {
              headerNames[index].style.opacity = "1";

            }
          }, 200);

          setTimeout(function() {
            if (index < position.length) {
              position[index].style.opacity = "1";
            }
          }, 400);
        }
      });
    }

    window.addEventListener('scroll', handleScroll);
  </script>





